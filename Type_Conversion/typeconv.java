class typeconv{
    public static void main(String args[]){
        float f = 5.6f;
        int a = (int)f;
        System.out.println(a);

        double d = 5.75;
        float x= (float)d;
        System.out.println(x);

        byte b=10;
        byte c=30;
        int e =b*c;
        System.out.println(e);
    }
}
